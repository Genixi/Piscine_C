int	ft_atoi(char *str)
{
	int	i;
	int	res;
	int	flag;

	i = 0;
	flag = 0;
	res = 0;
	if (str[i] == '-')
	{
		flag = 1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (flag == 1)
		return (-res);
	else
		return (res);
}
