int	is_letter(char c)
{
	if (((c >= 'A') && (c = 'Z')) || ((c >= 'a') && (c <= 'z')))
		return (1);
	else
		return (0);
}

int	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (is_letter(str[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
